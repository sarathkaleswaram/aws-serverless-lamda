'use strict';

const userCreate = require('./users-create.js');
const usersReadAll = require('./users-read-all.js');
const userReadOne = require('./users-read-one.js');
const userUpdate = require('./users-update.js');
const userDelete = require('./users-delete.js');

module.exports.add = (event, context, callback) => {
  userCreate(event, (error, result) => {
    const response = {
      statusCode: 200,
      headers: {
        "Access-Control-Allow-Origin" : "*"
      },
      body: JSON.stringify(result),
    };

    context.succeed(response);
  });
};

